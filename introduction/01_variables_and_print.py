# A variable gives a name to a value.
# This makes calculations easier to read and easier to change.

headmaster_name = "Albus Dumbledore"
number_of_houses = 4

print("Hogwarts School of Witchcraft and Wizardry")
print("Headmaster:", headmaster_name)
print("Number of houses:", number_of_houses)

# Python can calculate with numbers.
# Use ** for powers.

x = 3
y = x**2 - 2 * x - 3

print()
print("For x =", x)
print("y = x**2 - 2*x - 3 gives", y)

